                            	<h1>History, Demography and Progress</h1>
            
                                <p>Vancouver Hillel is based at the UBC campus and has been active at the University of British Columbia since 1947. Vancouver Hillel House opened at Simon Fraser University in 2005 and opened the doors to the Hillel House at the University  of Victoria in 2006. Each Vancouver Hillel campus runs under the leadership of a Vancouver Hillel staff person and a student board.</p>
                                
                                <p>Vancouver Hillel students come from all over the world. We estimate that 75% of all Jewish students in the Lower Mainland attend University or College, and that 30% of these Jewish students are participating in Hillel activities. It is Vancouver Hillel’s challenge to provide engaging and meaningful Jewish experiences for these young people during these formative years.  It is at this time in their lives that students make decisions as to the careers they want to pursue, reflecting on values they hold dear, and establish influential relationships.</p>
                                
                                <p>Since its foundational beginning in 2005, the Vancouver Hillel House at Simon Fraser University has grown exponentially. The success and growth of the SFU Vancouver Hillel House is truly inspirational. Volunteer-based student leadership provides stability and diversity to programming and inspires excellence. Student-initiated and staff-supported projects at SFU Hillel serve as models for the other campuses, especially with regards to Holocaust education and pro-active Israel advocacy.</p>
                                
                                <p>At the University of Victoria, an equally inspiring Vancouver Hillel legacy is being created. With the purchase of a family-sized house adjacent to the University in 2006, Jewish students attending UVic now have a space to call their own. With little time and with much enthusiasm, Jewish student leaders in Victoria, many of who hail from the Lower Mainland, have built two strong student boards and a creative program calendar. The Vancouver Hillel House at the University of Victoria is gaining a reputation for pioneer work in student leadership and Israel awareness programming. UVic Hillel House is home to hundreds of UVic students as well as an actual home for out-of-town students living in the house.</p>
                                
                                <p>The Capital Campaign for a new Vancouver Hillel House at UBC is an exhilarating project that will yield monumental results. The landmark three-story building being erected in the central locus point of UBC will serve as the Jewish center on campus for generations to come. A new building provides Hillel with the inopportune ability to extend its reach to more Jewish students and young adults and provides stability to the community. </p>
                                
                                <p>Vancouver Hillel is undergoing significant changes in the quality of student life and diversity in the student body.  Many of today’s UBC (and lower mainland college and university) Jewish students are coming to campus with varying levels of affiliation and Jewish knowledge.  Vancouver Hillel must continue to expand the diversity of opportunities presented to young Jewish students if the Jewish community is committed to maintaining and growing the Jewish community of Greater Vancouver.</p>
                                
                                <p>Vancouver Hillel’s Young Jewish Urbanites (HYJU) has grown steadily since its foundation in 2007. In September 2008, Vancouver Hillel founded HYJU: Professional Division (PD), an association for young, Jewish career people. Both groups flourish under the direction of a volunteer committee and a Vancouver Hillel staff person. Through the success of HYJU and HYJU: PD, Vancouver Hillel has proven to Hillel International that young Jewish adults are an important resource and partner, and stands alone as one of the only Hillels in the world that puts resources into this generation of Jewish leaders.</p>
                                
                                <p>The recent change of Vancouver Hillel’s mission statement from “Maximizing the number of Jewish students doing Jewish things with other Jews” to “Distinctively Jewish, universally human” has an immensely positive impact on the exponential growth of the Vancouver Hillel community. This direction allows for more complete outreach and also makes Vancouver Hillel extremely relevant to the campus and general community. This mission resonates with students. Furthermore, the caliber of Vancouver Hillel’s program staff is extremely impressive. Vancouver Hillel offers competitive employment benefits, attracting applicants from the Lower Mainland, other Canadian provinces and the United States; Vancouver Hillel is recognized throughout the community as a valuable place to work. This reputation of excellence creates a positive atmosphere of high staff retention rates and a happy, healthy work environment. </p>
                                
                                <p>Through a variety of programs at Vancouver Hillel, the Jewish community is showcased to the campus community and other non-Jewish groups affiliated with the Jewish community. Hillel conducts interfaith/multi-cultural programs, co-sponsors programs with other groups and hosts international speakers/ conferences on campus.</p>
                                
                                <p>Hillel serves as the Jewish agency on campus in all matters pertaining to campus/student issues and community–campus relations, including Israeli affairs. As such, we also work with Jewish faculty and staff on campus and provide an array of programming to reach out to this natural constituency of Hillel. We attempt to provide programming for faculty and staff that are meaningful and relevant and connect them to the Jewish community. Hillel approaches faculty and staff members to become part of the Hillel Board of Directors. </p>
                                
                                <p>The University of British Columbia, Simon Fraser University and the University of Victoria all have large Vancouverite populations; ultimately these young Jewish adults will define the future of our Jewish communities in Vancouver. As young professionals Vancouver Hillel students will soon be asked to populate the boardrooms of our synagogues and federation, lead our Jewish agencies and provide leadership to the secular community. Vancouver Hillel alumni will write the next chapter in the remarkable story of the Vancouver area Jewish people. Quite simply, a strong and vibrant Hillel at UBC, SFU and UVic today will ensure a strong Jewish community in B.C. for the future.</p>
                                
