                                <h2>CIE Birthright</h2> – visit Israel for free!
                                <a href="http://www.israelforfree.com" target="_blank">www.israelforfree.com</a>
                                <p>Taglit-Birthright Israel: Canada Israel Experience (CIE) provides the gift of first-time, peer group, educational trips to Israel for Jewish young adults, ages 18-26. Birthright Israel’s founders created this program to send thousands of young Jewish adults from all over the world to Israel because they believe that it is every Jewish person’s birthright to visit Israel. A unique partnership between the people of Israel through their government, local Jewish communities (North American Jewish Federations, through the United Jewish Communities, Keren Hayesod and the Jewish Agency for Israel) and leading philanthropists, Taglit-Birthright Israel provides a life-changing trip to all participants. To date, over 60,000 young adults have traveled to Israel for the first time on Birthright Israel trips – more than 1,000 from British Columbia alone.</p>
                                
                                <p>For more information, call Vancouver Hillel at 604 224 4748</p>
                                
                                <p>Return to <a href="#top">top</a></p>
                                
                                <br />
                                
                                <h2>March of the Living</h2>
                                <a href="http://www.marchoftheliving.org/" target="_blank">www.marchoftheliving.org</a>
                                <p>"When you listen to a witness, you become a witness."</p>
                                
                                <p>March of the Living, a Holocaust education program for Jewish students, is a trip epitomizing contrasts, friendships and learning. The trip begins in the death camps of Poland and finishes in Israel, allowing participants to experience the darkest epoch of European and Jewish history, followed by a celebration of redemption in the sovereign homeland of the Jewish people. All trips are accompanied by a local guide, driver and medic, a Holocaust survivor and a Canadian trip leader.</p>
                                
                                <p>Participants are carefully chosen. Acceptance is based on emotional maturity and community involvement. Students are also required to attend an interview in order to qualify for the program.</p>
                                
                                <p>Return to <a href="#top">top</a></p>
                                
                                <br />
                                
                                <h2>March of Remembrance and Hope</h2>
                                <a href="http://www.remembranceandhope.com/" target="_blank">www.remembranceandhope.com</a>
                                <p>"To remain silent and indifferent is the greatest sin of all" – Eli Wiesel</p>
                                
                                <p>Participation in the March of Remembrance and Hope, a Holocaust and genocide awareness trip open to non-Jewish students, marks the beginning of a lifelong journey. Students are challenged to speak out against intolerance and injustice; to refute the malevolence of Holocaust-deniers; to contribute to a better world; to make a positive difference. The inaugural March of Remembrance and Hope, consisting of 400 participants from 20 countries, took place in March 2001. Since that first trip, more than 1,000 students from around the world have participated in this experience.</p>
                                
                                <p>Return to <a href="#top">top</a></p>
                                
                                <br />
                                
                                <h2>Hasbara Fellowships</h2>
                                <a href="http://www.israelactivism.com/" target="_blank">www.israelactivism.com</a>
                                <p>Hasbara Fellowships, a program spearheaded by Aish International, educates and trains university students to be effective Israel activists on their campuses. Hasbara Fellowships, started in 2001 in conjunction with Israel's Ministry of Foreign Affairs, brings hundreds of students to Israel every summer and winter, giving them the information and tools to return to their campuses as leaders in the fight for Israel's right to exist peacefully as a Jewish state within secure borders. So far, Hasbara Fellowships has trained more than 1,400 students on more than 250 campuses. Upon returning from the program, Hasbara Fellows receive support from Hillel staff, to deliver effective campaigns, programs, speakers and other materials and tools. The organization focuses on college campuses, but the information and tools offered are applicable to other forums as well. Similarly, the leadership and presentation skills learned through Hasbara empower participants to become stronger, more successful leaders.</p>
                                
                                <p>Return to <a href="#top">top</a></p>
                                
