                                <h2>MASA: Gateway to Long-Term Israel Programs</h2>
                                <a href="http://www.masaisrael.org/" target="_blank">www.masaisrael.org</a>
                                <p>MASA enables young Jews (18 to 30) from around the world to build a lasting relationship with Israel, strengthen their Jewish identity and gain meaningful experiences by participating in a long-term program in Israel. MASA provides young Jewish adults with connections to programs, grants and scholarships towards program fees, as well as support activities, workshops and resources while in Israel.</p>
                                
                                <p>MASA is the culmination the dream expressed by former prime minister Ariel Sharon: that all Jews should spend a semester or a year in Israel. Officially established by the government of Israel and the Jewish Agency for Israel in 2003, MASA is your gateway to more than 150 long-term programs in Israel.</p>
                                
