                            	<h1>Help Build B.C.’s Jewish Future!</h1>
            
                                <h2>New Hillel House at UBC Needs Community Support</h2>
                                
                                <p>Hillel launched a Capital Campaign on December 3, 2006, to raise funds for a new building at UBC. The Campaign comes on the heels of an unprecedented offer from the university, which will see Vancouver Hillel remain at the heart of campus until at least 2084. Sufficient funds have been raised from generous community members to begin construction and the new building is slated to open in time for the 2010-11 academic year.</p>
                                
                                <p><q>The redevelopment of the UBC campus presents an enviable possibility for us,</q> says Isaac Thau, Chair of Vancouver Hillel’s Capital Campaign. <q>The present Hillel House does not suit the plans UBC has for its environment. The university administration has said that, if we build a structure that is in keeping with the changes on campus, they will extend our lease for eight decades.</q></p>
                                
                                <p>Thau, a member of Hillel International’s Board of Governors, says the extraordinary offer came at a time when Hillel is confronting the shortcomings of its current building.</p>
                                
                                <p><q>We have gone from a small Hillel, with about 100 engaged students, to one of the fastest-growing Hillels in the world, with a stunning 1,000% leap in student engagement and commensurate increases in programming," says Thau. "UBC’s offer could not have come at a more opportune time.</q></p>
                                
                                <p>In addition to a massive increase in student engagement at UBC, Hillel has expanded its services to Simon Fraser University, where it opened the first Hillel House in 2005, and to the University of Victoria, which opened the doors of its new Hillel House in September 2006. The primary administrative support functions for Hillel throughout British Columbia are based at Hillel House at UBC.</p>
                                
                                <p>In May 2009, the 62-year-old Hillel House, a Second World War-era <q>portable</q> hut that stood in the same location at the heart of the University of B.C. campus since 1947, was demolished to make room for construction of the landmark new facility.</p>
                                
                                <p><q>We already have, arguably, the best location of any Hillel in the world,</q> Thau says. <q>To guarantee we can keep it until 2084, we need to build this new facility.</q></p>
                                
                                <p>Hillel continues to fundraise for the Capital Campaign and to provide an endowment for programming in the new Hillel House at UBC and across British Columbia.</p>
                                
                                <p>To help create a legacy for generation after generation of Jewish students – and to make B.C. even more of a destination for Jewish students – please call Vancouver Hillel at 604 224 4748 or fill out the form to the right. We'll contact you.</p>
                    
